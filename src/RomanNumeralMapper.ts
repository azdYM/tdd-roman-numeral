export class RomanNumeralMapper {
    public oneToRomanNumeral() {
        return 'I'
    }

    public fiveToRomanNumeral() {
        return 'V'
    }

    public tenToRomanNumeral() {
        return 'X'
    }

    public fiftyToRomanNumeral() {
        return 'L'
    }

    public oneHundredToRomanNumeral() {
        return 'C'
    }
}